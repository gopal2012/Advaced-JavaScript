let a = 5;
let b = '5';
let c = 10;

console.log("a == b:", a == b);       // true (values equal, types ignored)
console.log("a === b:", a === b);     // false (different types)
console.log("a != b:", a != b);       // false
console.log("a !== b:", a !== b);     // true
console.log("c > a:", c > a);         // true
console.log("c < a:", c < a);         // false
console.log("a >= 5:", a >= 5);       // true
console.log("a <= 4:", a <= 4);       // false

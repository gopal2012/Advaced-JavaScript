// 1. Basic assignment
let a = 10;
console.log("\n1. a = 10 →", a);

// 2. Add and assign (+=)
a += 5;  // a = 10 + 5
console.log("2. a += 5 →", a);

// 3. Subtract and assign (-=)
a -= 3;  // a = 15 - 3
console.log("3. a -= 3 →", a);

// 4. Multiply and assign (*=)
a *= 2;  // a = 12 * 2
console.log("4. a *= 2 →", a);

// 5. Divide and assign (/=)
a /= 3;  // a = 24 / 3
console.log("5. a /= 3 →", a);

// 6. Modulus and assign (%=)
a %= 5;  // a = 8 % 5
console.log("6. a %= 5 →", a);

// 7. Exponentiation and assign (**=)
a = 2;
a **= 3;  // a = 2^3
console.log("7. a **= 3 →", a);